package utils

// IsContains returns True if given slice contains given string item.
// Otherwise, returns False.
func IsContains(slice []string, item string) bool {
	set := make(map[string]struct{}, len(slice))
	for _, s := range slice {
		set[s] = struct{}{}
	}

	_, ok := set[item]
	return ok
}
